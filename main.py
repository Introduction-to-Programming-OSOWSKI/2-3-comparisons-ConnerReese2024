def greaterThan(x, y):
    if x > y:
        return True
    else: 
        return False


def lessThan(x, y):
    if x < y:
        return True
    else:
        return False


def equalTo(x, y):
    if x == y:
        return True
    else:
        return False


def greaterOREqualTo(x, y):
    if x >= y:
        return True
    else:
        return False


def lessOrEqualTo(x, y):
    if x <= y:
        return True
    else:
        return False